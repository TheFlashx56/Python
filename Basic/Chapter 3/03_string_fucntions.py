# --- function to find the length of string----
string = "example"
print(len(string))


## function to check the the variable endswith some certain keywords
# endswith("")-> Return True if S ends with the specified suffix, False otherwise.
print(string.endswith("ple"))

# startswith("")-> Return True if S starts with the specified suffix, False otherwise.
print(string.startswith("ex"))

# string.capitalize -> Return a capitalized version of the string.
print(string.capitalize())

